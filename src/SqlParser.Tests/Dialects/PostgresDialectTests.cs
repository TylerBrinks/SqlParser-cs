            "ALTER TABLE tab ALTER COLUMN is_active SET DATA TYPE TEXT USING 'text'");

        var alter = VerifiedStatement<Statement.AlterTable>(
            "ALTER TABLE tab ALTER COLUMN is_active SET DATA TYPE TEXT USING 'text'");
